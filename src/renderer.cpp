void Renderer::render_scene(Shader &shader)
{
    // floor
    glm::mat4 mat_model = glm::mat4(1.0f);
    shader.setMat4("model", mat_model);
    render_plane();

    // cubes
    mat_model = glm::mat4(1.0f);
    mat_model = glm::translate(mat_model, glm::vec3(0.0f, 1.5f, 0.0));
    mat_model = glm::scale(mat_model, glm::vec3(0.5f));
    shader.setMat4("model", mat_model);
    render_cube();

    mat_model = glm::mat4(1.0f);
    mat_model = glm::translate(mat_model, glm::vec3(2.0f, 0.0f, 1.0));
    mat_model = glm::scale(mat_model, glm::vec3(0.5f));
    shader.setMat4("model", mat_model);

    render_cube();
    mat_model = glm::mat4(1.0f);
    mat_model = glm::translate(mat_model, glm::vec3(-1.0f, 0.0f, 2.0));
    mat_model = glm::rotate(mat_model, glm::radians(60.0f), glm::normalize(glm::vec3(1.0, 0.0, 1.0)));
    mat_model = glm::scale(mat_model, glm::vec3(0.25));
    shader.setMat4("model", mat_model);
    render_cube();

    // loaded model
    mat_model = glm::mat4(1.0f);
    mat_model = glm::translate(mat_model, glm::vec3(0.0f, 0.0f, 0.0f)); // translate it down so it's at the center of the scene
    mat_model = glm::scale(mat_model, glm::vec3(1.0f, 1.0f, 1.0f));	// it's a bit too big for our scene, so scale it down
    shader.setMat4("model", mat_model);
    model->Draw(shader);
}

